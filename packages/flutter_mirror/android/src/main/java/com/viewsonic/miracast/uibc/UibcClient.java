  public UibcClient(UibcSender sender) {
    sender_ = sender;
    for (int i = 0; i < MAX_TOUCH_NUM; i++) {
      touch_infos_[i] = new TouchInfo();
    }
  }

  public void start() {
    sendDataDescriptor();
  }

  public void pause() {
    paused_ = true;
  }

  public void resume() {
    paused_ = false;
  }

  public void generateTouchData() {
    final byte[] UIBC_PACKET_HEADER = {
        0x00, // 00: timestamp not exist
        0x01, // 01:HIDC
        0x00, TOUCH_DATA_SIZE // 0x0049:Length
    };
    final byte[] UIBC_INPUT_BODY_HEADER = {
        0x01, // 01:USB
        0x03, // 03:Multi-touch
        0x00, // 00:INPUT REPORT
        0x00, TOUCH_DATA_SIZE - 9, // 0x0040:Length
        0x13 // 0x13:Report ID
    };

    /* reset data of uibcDataBytes_ to 0x00 */
    for (int i = 0; i < TOUCH_DATA_SIZE; i++) {
      uibcDataBytes_[i] = 0x00;
    }

    /* copy UIBC Headers to uibcDataBytes_ */
    System.arraycopy(UIBC_PACKET_HEADER, 0, uibcDataBytes_, 0, UIBC_PACKET_HEADER.length);
    System.arraycopy(UIBC_INPUT_BODY_HEADER, 0, uibcDataBytes_, UIBC_PACKET_HEADER.length,
        UIBC_INPUT_BODY_HEADER.length);
    /* copy touch data to uibcDataBytes_ */
    int offset = UIBC_PACKET_HEADER.length + UIBC_INPUT_BODY_HEADER.length;
    byte hidTouchId = 0;
    for (int i = 0; i < MAX_TOUCH_NUM; i++) {
      if (touch_infos_[i].id_ != -1) {
        uibcDataBytes_[offset] = (byte) (touch_infos_[i].down_ ? 0x01 : 0x00);
        uibcDataBytes_[offset + 1] = (byte) i;// hidTouchId;
        int posX = (int) (touch_infos_[i].x_ * 0x7FFF);
        int posY = (int) (touch_infos_[i].y_ * 0x7FFF);
        uibcDataBytes_[offset + 2] = (byte) (posX & 0xFF);
        uibcDataBytes_[offset + 3] = (byte) ((posX >> 8) & 0xFF);
        uibcDataBytes_[offset + 4] = (byte) (posY & 0xFF);
        uibcDataBytes_[offset + 5] = (byte) ((posY >> 8) & 0xFF);
        hidTouchId++;
        offset += 6;
      }
    }
    offset = UIBC_PACKET_HEADER.length + UIBC_INPUT_BODY_HEADER.length + 6 * MAX_TOUCH_NUM;
    // Log.d(TAG, "Touch count:" + hidTouchId);
    uibcDataBytes_[offset] = hidTouchId;
    /* update scan time and save to 16bit integer */
    short scanTime = (short) (System.currentTimeMillis() / 10);
    uibcDataBytes_[offset + 1] = (byte) (scanTime & 0xFF);
    uibcDataBytes_[offset + 2] = (byte) ((scanTime >> 8) & 0xFF);
  }

  public void onTouchEvent(int touchId, boolean touchDown, double x, double y) {
    int touchInfoIdx = -1;
    int emptyIdx = -1;
    for (int i = 0; i < MAX_TOUCH_NUM; i++) {
      if (emptyIdx == -1 && touch_infos_[i].id_ == -1) {
        emptyIdx = i;
      }

      if (touch_infos_[i].id_ == touchId) {
        touchInfoIdx = i;
        break;
      }
    }

    if (touchInfoIdx == -1) {
      if (touchDown == false || emptyIdx == -1) {
        return;
      }
      touchInfoIdx = emptyIdx;
      Log.d(TAG, "put touch slot:" + touchInfoIdx + " id:" + touchId);
    }

    touch_infos_[touchInfoIdx].id_ = touchId;
    touch_infos_[touchInfoIdx].down_ = touchDown;
    touch_infos_[touchInfoIdx].x_ = x;
    touch_infos_[touchInfoIdx].y_ = y;

    long currentTime = System.currentTimeMillis();
    if (paused_ == false && currentTime - lastTouchDataSendTime_ > 10) {
      lastTouchDataSendTime_ = currentTime;
      generateTouchData();
      sendTouchEvent();
    }

    if (touchDown == false) {
      touch_infos_[touchInfoIdx].id_ = -1;
      Log.d(TAG, "remove touch slot:" + touchInfoIdx + " id:" + touchId);
    }
  }

  private void sendTouchEvent() {
    // Log.d(TAG, "sendUibcData");
    sender_.sendUibcData(uibcDataBytes_);
  }

