  private Runnable stopConnectRunnable = new Runnable() {
    @Override
    public void run() {
      Log.d(TAG, "stop UIBC.");
      cleanResource();
    }
  };

  private void tryConnect() {
    try {
      if (curConnTime_ >= MAX_CONN_TIME) {
        Log.d(TAG, "try connect to the UIBC Socket " + MAX_CONN_TIME + " time failed.");
        return;
      }

      Log.d(TAG, "start try to connect the UIBC Socket,"
          + "socket host:" + uibcHost_ + ", port:" + uibcPort_);

      curConnTime_++;

      curState_ = STATE_STARTING;

      uibcSocket_ = new UibcSocket(uibcHost_, uibcPort_);
      int ret = uibcSocket_.start();
      if (ret == 0) {
        curState_ = STATE_STARTED;
        handler_.post(sendDataRunnableDescriptor);
      } else {
        curState_ = STATE_STOPPED;
        handler_.postDelayed(startConnectRunnable, RETRY_CONN_INTERVAL);
      }
    } catch (Exception e) {
      curState_ = STATE_STOPPED;
      Log.e(TAG, "tryConnect Exception:" + e.toString());
    }
  }

  private void cleanResource() {
    try {
      curState_ = STATE_STOPPED;

      handler_.removeCallbacksAndMessages(null);

      if (uibcSocket_ != null) {
        Log.d(TAG, "stop UIBC socket.");
        uibcSocket_.close();
      }
    } catch (Exception e) {
      Log.d(TAG, "UIBC stop() Exception: " + e.toString());
    }
  }
